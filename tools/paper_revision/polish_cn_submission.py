EXPECTED_MARKERS = {
    8: "引言",
    17: "相关工作",
    26: "方法",
    76: "实验",
    120: "讨论",
    124: "结论",
    143: "参考文献",
}


def update_paragraph(doc, index_1based, text):
    paragraph = doc.paragraphs[index_1based - 1]
    if paragraph.runs:
        paragraph.runs[0].text = text
        for run in paragraph.runs[1:]:
            run.text = ""
    else:
        paragraph.text = text


def validate_markers(doc):
    for index_1based, expected in EXPECTED_MARKERS.items():
        actual = doc.paragraphs[index_1based - 1].text.strip()
        if actual != expected:
            raise RuntimeError(
                f"Paragraph {index_1based} marker mismatch: expected '{expected}', got '{actual}'"
            )


def extract_body_text(doc):
    return "\n".join(para.text.strip() for para in doc.paragraphs[:142] if para.text.strip())


def extract_reference_numbers(doc):
    refs = []
    for para in doc.paragraphs[143:]:
        text = para.text.strip()
        if text:
            match = re.match(r"\[(\d+)\]", text)
            if match:
                refs.append(int(match.group(1)))
    return refs


def figure_table_mentions(text):
    figures = sorted({int(x) for x in re.findall(r"图\s*(\d+)", text)})
    tables = sorted({int(x) for x in re.findall(r"表\s*(\d+)", text)})
    cites = sorted({int(x) for x in re.findall(r"\[(\d+)\]", text)})
    return figures, tables, cites


def count_math_objects(docx_path):
    ns = {
        "m": "http://schemas.openxmlformats.org/officeDocument/2006/math",
    }
    with zipfile.ZipFile(docx_path) as zf:
        root = ET.fromstring(zf.read("word/document.xml"))
    return len(root.findall(".//m:oMath", ns)) + len(root.findall(".//m:oMathPara", ns))


