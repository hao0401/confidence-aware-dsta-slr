function Get-ParagraphText {
    param(
        [System.Xml.XmlNode]$Paragraph,
        [System.Xml.XmlNamespaceManager]$Ns
    )

    $texts = $Paragraph.SelectNodes('.//w:t', $Ns) | ForEach-Object { $_.'#text' }
    return (($texts -join '') -replace '\s+', ' ').Trim()
}

function Set-ParagraphText {
    param(
        [System.Xml.XmlNode]$Paragraph,
        [System.Xml.XmlNamespaceManager]$Ns,
        [string]$NewText
    )

    $doc = $Paragraph.OwnerDocument
    $firstRun = $Paragraph.SelectSingleNode('./w:r', $Ns)
    $runProps = $null
    if ($firstRun -and $firstRun.SelectSingleNode('./w:rPr', $Ns)) {
        $runProps = $firstRun.SelectSingleNode('./w:rPr', $Ns).CloneNode($true)
    }

    $runs = @($Paragraph.SelectNodes('./w:r', $Ns))
    foreach ($run in $runs) {
        [void]$Paragraph.RemoveChild($run)
    }

    $newRun = $doc.CreateElement('w', 'r', $Ns.LookupNamespace('w'))
    if ($runProps) {
        [void]$newRun.AppendChild($doc.ImportNode($runProps, $true))
    }
    $textNode = $doc.CreateElement('w', 't', $Ns.LookupNamespace('w'))
    $textNode.InnerText = $NewText
    [void]$newRun.AppendChild($textNode)
    [void]$Paragraph.AppendChild($newRun)
}

function Find-ParagraphByText {
    param(
        [System.Collections.IEnumerable]$Paragraphs,
        [System.Xml.XmlNamespaceManager]$Ns,
        [string]$ExpectedText
    )

    foreach ($paragraph in $Paragraphs) {
        if ((Get-ParagraphText -Paragraph $paragraph -Ns $Ns) -eq $ExpectedText) {
            return $paragraph
        }
    }

    throw "Paragraph not found: $ExpectedText"
}

function Insert-ParagraphAfter {
    param(
        [System.Xml.XmlNode]$ReferenceParagraph,
        [System.Xml.XmlNode]$TemplateParagraph,
        [System.Xml.XmlNamespaceManager]$Ns,
        [string]$NewText
    )

    $clone = $TemplateParagraph.CloneNode($true)
    Set-ParagraphText -Paragraph $clone -Ns $Ns -NewText $NewText
    [void]$ReferenceParagraph.ParentNode.InsertAfter($clone, $ReferenceParagraph)
    return $clone
}

